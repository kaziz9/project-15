export const getTranslation = (language: 'ar' | 'en', key: string): string => {
  const keys = key.split('.');
  let value: any = translations[language];
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  return value || key;
};

export const t = (language: 'ar' | 'en', key: string, replacements?: Record<string, string>): string => {
  let translation = getTranslation(language, key);
  
  if (replacements) {
    Object.entries(replacements).forEach(([placeholder, replacement]) => {
      translation = translation.replace(`{${placeholder}}`, replacement);
    });
  }
  
  return translation;
};